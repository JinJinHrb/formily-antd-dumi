export interface TCustomerListResponse {
	data: TCustomerData[]
	totalCount: number
}

export const listCustomer = (
	customerName: string,
	pageNo: number,
	pageSize: number,
): Promise<TCustomerListResponse> => {
	const mockData = customerName ? MOCK_DATA.list.filter(a => a.customerName === customerName) : MOCK_DATA.list
	const data = clone(mockData.slice((pageNo - 1) * pageSize, pageNo * pageSize))
	for (let i = 0; i < Math.min(data.length, 10); i++) {
		if (data?.[i]) {
			data[i] = clone(data[i])
			if (data[i]?.customerName) {
				data[i].customerName = data[i].customerName + '-' + Math.floor(Math.random() * 1000)
			}
		}
	}

	return new Promise(rsv => {
		setTimeout(() => {
			rsv({
				data,
				totalCount: mockData.length,
			})
		}, 1000)
	})
}

